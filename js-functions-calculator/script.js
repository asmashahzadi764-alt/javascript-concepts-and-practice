//Add Function
function Add(a,b){
    return a + b;
}

//Subtract Function (Arrow)
const Subtract =(a,b) =>{
     return a - b;
}

//Multiply Function
function Multiply(a,b){
    return a * b;
}

//Divide Function (Arrow)
const Divide = (a, b) => {
    return a/b;
}

console.log("Add:", Add(10,5));
console.log("Subtract:", Subtract(10,5));
console.log("Multiply:",Multiply(10,5));
console.log("Divide:",Divide(10,5));
